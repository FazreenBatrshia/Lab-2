
public class SumValues {

	public static void main(String[] args) {
		// Declare and initialize variables
		int value1=25;
		double value2=22.5;
		
		// Convert value1 from int to double
		double newValue1 = (double) value1;
		
		// Add newValue1 and value2
		double sumValues = newValue1 + value2;
		System.out.println("The sum of " + newValue1 + " and " + value2 + " is: " + sumValues);
		

	}

}
